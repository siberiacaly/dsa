package cviko2;

public class folder {
	private folder left;
	private folder right;
	
	
	public folder getLeft() {
		return left;
	}
	public void setLeft(folder left) {
		this.left = left;
	}
	public folder getRight() {
		return right;
	}
	public void setRight(folder right) {
		this.right = right;
	}
}
